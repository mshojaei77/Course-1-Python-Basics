# String Data Type رشته 
name = "Tanjiro"
print(name)
print(type(name))

#Integer Data Type عدد صحیح
age = 15
print(age)
print(type(age))

#Float Data Type عدد اعشاری
height = 1.65 
print(height)
print(type(height))

#Complex Data Type عدد مختلط
complex_num = 3 + 4j
print(complex_num)
print(type(complex_num))

#Boolean Data Type نوع داده بولی
is_strong = True
print(is_strong)
print(type(is_strong))
