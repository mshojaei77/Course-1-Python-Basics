# Numeric methods for integers and floats
x = 5
y = 2.5
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x // y)
print(x % y)
print(x ** y)
print(abs(-x))
print(divmod(x, y))
print(pow(x, y))
print(round(y, 1))
print(int(y))
print(float(x))

# Numeric methods for complex numbers
a = 2 + 3j
b = 4 - 2j
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(abs(a))
print(a.real)
print(a.imag)
print(a.conjugate())

# Numeric methods for bitwise operations
x = 5
y = 3
print(x & y)
print(x | y)
print(x ^ y)
print(~x)
print(x << 2)
print(x >> 1)

# Numeric methods for math functions
import math
print(math.ceil(y))
print(math.floor(y))
print(math.sqrt(x))
print(math.exp(x))
print(math.log(x))
print(math.log10(x))
print(math.sin(y))
print(math.cos(y))
print(math.tan(y))
print(math.degrees(y))
print(math.radians(60))

